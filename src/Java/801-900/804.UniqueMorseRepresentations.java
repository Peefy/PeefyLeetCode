class Solution {
    public int uniqueMorseRepresentations(String[] words) {
        String[] morse = new String[]{".-","-...","-.-.","-..",".","..-.","--.","....","..",".---","-.-",".-..","--","-.","---",".--.","--.-",".-.","...","-","..-","...-",".--","-..-","-.--","--.."};
        HashSet<String> morsewords = new HashSet<>();  
        for(String word : words){
            String l = "";
            for (char c : word.toCharArray())
                l += morse[c - 'a'];
            morsewords.add(l);
        }
        return morsewords.size();
    }
}
